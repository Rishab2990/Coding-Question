public class starSquarePattern {
    public static void main(String[] args) {
        
        // print square pattern using "*"

        int k = 5;
        for (int l = 0; l < k; l++) {
            for (int m = 0; m < k; m++) {
                System.out.print("*" + " ");
            }
            System.out.println();

        }

    }
}
